function getPozos(req, res) {
    Pozo.find({})
        .populate('estado.estadoPozo')
        .populate('relevador')
        .exec((err, pozos) => {
            if (err) {
                return res.status(404).json({
                    title: 'Error',
                    error: err
                });
            }
            if (!pozos) {
                return res.status(404).json({
                    title: 'Error',
                    error: err
                });
            }
            res.status(200).json({
                message: 'Success',
                obj: pozos
            });
        });
}

function getPozo(req, res) {
    Pozo.findById({
            '_id': req.params.idPozo
        })
        .exec(function (err, pozo) {
            if (err) {
                return res.status(400).json({
                    title: 'Error',
                    error: err
                });
            }
            if (!pozo) {
                return res.status(404).json({
                    title: 'Error',
                    error: err
                });
            }
            res.status(200).json({
                message: 'Success',
                obj: pozo
            });
        });
}

